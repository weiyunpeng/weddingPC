<template>
    <transition name="photo_modal">
        <div class="photo_modal text-center" v-show="value && loading">
            <div class="photo_modal_dialog panel text-left">
                <img @click="close" class="close_btn" src="/static/images/icon-close-hover.png">
                <div class="loadingmask" id="loadmask"></div>
                <div class="photo_fl fl">
                    <swiper :options="swiperOption" ref="mySwiper">
                        <swiper-slide class="photo_fl fl" v-for="(item,flowNum) in getViewPhoto" v-bind:key="flowNum">
                         <div class="imgcrop"> <img :src="item.img" height="475"></div>
                            <span v-if="isCase" class="pg_like" @click="photoLikeBtn(item.id,item.is_fav,flowNum)">
                                <i v-if="item.is_fav" class="icon_like_act"></i>
                                <i v-if="!item.is_fav" class="icon_like"></i>
                                {{item.fav_num}}
                            </span>
                        </swiper-slide>
                        <div class="swiper-button-prev" slot="button-prev" @click="changeWaterPrev()"></div>
                        <div class="swiper-button-next" slot="button-next" @click="changeWaterNext()"></div>
                    </swiper>
                    <div class="modal-details">
                        <span>{{photoInfo.time}}</span>
                        <label>{{photoInfo.desc}}</label>
                        <label style="display:block">你也可以联系 喜宝 （微信号：jiehun5259）来分享你的结婚故事.<a v-if="photoInfo.comment_url"
                                                                                            :href="photoInfo.comment_url"
                                                                                            target="_blank">TA的拍摄历程>>></a></label>
                    </div>
                </div>

                <p class="pg_name">TA的套系</p>

                <div class="photo_fr fr">

                    <waterfall :line-gap="200" :min-line-gap="140" :max-line-gap="140" :single-max-width="140"
                               :watch="getViewPhoto">
                        <waterfall-slot v-for="(item, index) in getViewPhoto" :width="item.width" :height="item.height"
                                        :order="index" :key="item.index" move-class="photo_move">
                            <img :src="item.img" class="water_img" v-bind:class="{cur: selected == index}"
                                 @click="changeSwiper(item,index)">
                        </waterfall-slot>
                    </waterfall>
                </div>
            </div>
        </div>
    </transition>
</template>

<script>
import { waterfall, waterfallSlot } from 'vue-waterfall';
import { swiper, swiperSlide } from 'vue-awesome-swiper';
import { mapGetters, mapActions } from 'vuex';

export default {
    components: {
        waterfall: waterfall,
        waterfallSlot: waterfallSlot,
        swiper: swiper,
        swiperSlide: swiperSlide
    },
    data() {
        return {
            loadshow: true,
            selected: 0,
            loading: false,
            photoInfo: {},
            isCase: true,
            swiperOption: {
                notNextTick: true,
                setWrapperSize: false,
                slidesPerView: 1,
                paginationClickable: false,
                mousewheelControl: false,
                observeParents: true,
                prevButton: '.swiper-button-prev',
                nextButton: '.swiper-button-next',
                preloadImages: false,
                lazyLoading: true,
                onTransitionStart(swiper) {
                    this.index = swiper.realIndex;
                }
            }
        };
    },
    props: {
        value: {
            default: true
        },
        photoModal: {
            type: Object,
            default: null
        },
        order: {
            type: Number,
            default: null
        }
    },
    computed: {
        ...mapGetters({
            getViewPhoto: 'getViewPhoto',
            getViewPhotoInfo: 'getViewPhotoInfo',
            getViewPhotoStatus: 'getViewPhotoStatus'
        }),
        ...mapActions({
            showModal: 'showModal'
        }),
        swiper: {
            get: function () {
                return this.$refs.mySwiper.swiper;
            },
            set: function () {}
        }
    },
    mounted() {
        this.swiper.slideTo(0, 1000, false);
    },
    methods: {
        close() {
            setTimeout(function() {
                document.getElementById('loadmask').style.display = 'block';
            }, 500);
            this.loading = false;
            this.swiper.slideTo(0, 1000, false);
            this.selected = 0;
            this.$emit('input', false);
        },
        changeSwiper(item, index) {
            this.selected = index;
            this.swiper.slideTo(index, 1000, false);
        },
        changeWaterPrev() {
            this.selected = this.swiper.realIndex - 1;
        },
        changeWaterNext() {
            this.selected = this.swiper.realIndex + 1;
        },
        photoLikeBtn(id, fav, flowNum) {
            let token = JSON.parse(localStorage.getItem('user'));
            let isLogin = Boolean(token);
            if (isLogin) {
                // let obj = this.getViewPhoto[flowNum]
                // obj.is_fav = !obj.is_fav
                // this.$set(this.getViewPhoto, flowNum, obj);
                if (fav == 0) {
                    //说明未收藏，可以收藏
                    let ajaxdata = {
                        id: id,
                        uid: token.uid,
                        index: flowNum,
                        order: this.order,
                        is_fav: fav,
                        isFill: this.photoModal.isFill
                    };
                    this.$store.dispatch('collectPhoto', ajaxdata);
                } else if (fav == 1) {
                    //说明已经收藏了,为取消收藏
                    let ajaxdata = {
                        id: id,
                        uid: token.uid,
                        index: flowNum,
                        order: this.order,
                        is_fav: fav,
                        isFill: this.photoModal.isFill
                    };
                    this.$store.dispatch('cancelCollectPhoto', ajaxdata);
                } else {
                    //未知异常
                    console.log('collect 接口异常');
                }
            } else {
                const data = {
                    name: 'delPhoto',
                    info: {
                        text: '登录后才能收藏哦~'
                    }
                };
                this.$store.dispatch('showModal', data);
            }
        },
        swiper(a) {}
    },
    watch: {
        value() {
            if (this.value) {
                const self = this;
                const tempImage = new Image();
                tempImage.onload = function() {
                    self.loading = true;
                };
                tempImage.onerror = function() {
                    self.close();
                };
                tempImage.src = self.photoModal.img;
            }
        },
        getViewPhotoInfo() {
            console.log('图集加载完成');
            document.getElementById('loadmask').style.display = 'none';
            setTimeout(function() {
                document.getElementById('loadmask').style.display = 'none';
            }, 1500);
            if (this.getViewPhotoInfo) {
                this.photoInfo = this.getViewPhotoInfo;
            }
        },
        getViewPhotoStatus() {
            const self = this;
            if (this.getViewPhotoStatus == -1) {
                setTimeout(() => self.close(), 2000);
            }
        },
        photoModal() {
            if (this.photoModal.src) {
                this.isCase = false;
            }
        }
    }
};
</script>

